package com.joaosales.cursojava.aula11;

public class VariaveisLiterais {

	public static void main(String[] args) {
		
		int decVal = 26;
		int hexVal = 0x1a;
		int octVal = 032;
		int binVal = 0b11010;
		
		System.out.println(decVal);
		System.out.println(hexVal);
		System.out.println(octVal);
		System.out.println(binVal);
		
		
		int oct31 = 031;
		int Dec25 = 25;
		
		System.out.println("============");
		System.out.println(oct31 == Dec25);
		
//		 Sequecia de Escape 
//		
//		\t - tab 
//		\b - espaço
//		\n - enter
//		\r - retorno de carro
//		\f - avanço de página
//		\' - aspas simples
//		\" - aspas duplas 
//		\\ - barra invertida
//		\ddd - constante octal
//		\\uxxxx - constante hexadecimal
		
		
	}

}
